#include<iostream>
using namespace std;

int main(){

    cout<<"answer of 6&10 is: "<<(6 & 10)<<endl;
    cout<<"answer of 6|10 is: "<<(6 | 10)<<endl;
    cout<<"answer of 6^10 is: "<<(6 ^ 10)<<endl;

    cout<<"answer of 10<<2 is: "<<(10 << 2)<<endl;
    cout<<"answer of 10>>1 is: "<<(10 >> 1)<<endl;
    
    return 0;
}